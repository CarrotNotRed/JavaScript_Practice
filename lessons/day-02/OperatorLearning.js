let roundingNearest = Math.round(10/3);
console.log(`Rounding Nearest: ` + roundingNearest);

let roundingFloor = Math.floor(10/3);
console.log(`Rounding Floor: ` + roundingFloor);

let roundingCeil = Math.ceil(10/3);
console.log(`Rounding Ceil: ` + roundingCeil);

let result = 10/3;
let roundingFixedNumber = result.toFixed(2);
